# ─── topic_slug ───────────────────────────────────────────────


def test_slug_lowercase_hyphens():
    """Slug is lowercase with hyphens."""
    assert topic_slug("Machine Learning") == "machine-learning"


def test_slug_handles_unicode():
    """Unicode chars are normalized to ASCII."""
    slug = topic_slug("Réseau Neuronal")
    assert slug.isascii()
    assert "reseau" in slug


def test_slug_removes_special_chars():
    """Special/unsafe characters are removed."""
    slug = topic_slug("C++ / C# <Programming>")
    assert "<" not in slug
    assert ">" not in slug
    assert "/" not in slug
    assert "#" not in slug


def test_slug_truncates_to_50():
    """Slug is truncated to 50 characters max."""
    slug = topic_slug("a" * 100)
    assert len(slug) <= 50


def test_slug_strips_leading_trailing_hyphens():
    """Leading and trailing hyphens are stripped."""
    slug = topic_slug("---hello---")
    assert slug == "hello"


def test_slug_collapses_multiple_hyphens():
    """Multiple consecutive hyphens are collapsed to one."""
    slug = topic_slug("hello   world!!!test")
    assert "hello-world-test" == slug


def test_slug_empty_input_returns_fallback():
    """Pure non-ASCII input that produces empty slug returns 'research' fallback."""
    slug = topic_slug("\u4e16\u754c")  # Chinese characters
    assert slug == "research"


