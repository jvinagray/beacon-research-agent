"""Tests for server.export module."""
import json

from beacon.models import EvaluatedSource, Flashcard, IntelligenceSignals, ResearchResult
from server.export import generate_markdown, topic_slug


# ─── Helper ───────────────────────────────────────────────────


def _make_source(title: str, score: int, **kw) -> EvaluatedSource:
    """Create an EvaluatedSource with minimal boilerplate."""
    return EvaluatedSource(
        url=kw.get("url", f"https://example.com/{title.lower().replace(' ', '-')}"),
        title=title,
        snippet=kw.get("snippet", f"Snippet for {title}"),
        signals=IntelligenceSignals(
            learning_efficiency_score=score,
            content_type=kw.get("content_type", "tutorial"),
            time_estimate_minutes=kw.get("time_estimate", 15),
            recency=kw.get("recency", "2024"),
            key_insight=kw.get("key_insight", f"Key insight about {title}"),
            coverage=kw.get("coverage", ["topic-a", "topic-b"]),
        ),
    )


def _make_result(**kw) -> ResearchResult:
    """Create a ResearchResult with sensible defaults."""
    return ResearchResult(
        topic=kw.get("topic", "Machine Learning"),
        depth=kw.get("depth", "standard"),
        sources=kw.get("sources", [_make_source("Default Source", 7)]),
        artifacts=kw.get("artifacts", {}),
        session_id=kw.get("session_id", "test-session"),
        timestamp=kw.get("timestamp", "2024-06-15T10:00:00Z"),
    )


# ─── Happy path ───────────────────────────────────────────────


def test_header_includes_topic_depth_date_source_count():
    """Header contains topic, depth, source count, and date."""
    sources = [_make_source("S1", 5), _make_source("S2", 8), _make_source("S3", 3)]
    result = _make_result(sources=sources)
    md = generate_markdown(result)
    assert md.startswith("# Research: Machine Learning")
    assert "standard" in md
    assert "3" in md  # source count
    assert "2024-06-15" in md  # date


def test_includes_executive_summary():
    """Executive Summary section appears with summary text."""
    result = _make_result(artifacts={"summary": "This is the summary text."})
    md = generate_markdown(result)
    assert "## Executive Summary" in md
    assert "This is the summary text." in md


def test_includes_sources_with_signals():
    """Sources section includes all intelligence signal fields."""
    source = _make_source(
        "Deep Learning Guide",
        score=9,
        content_type="tutorial",
        time_estimate=20,
        recency="2024",
        key_insight="Transformers are key",
        coverage=["neural-nets", "attention"],
    )
    result = _make_result(sources=[source])
    md = generate_markdown(result)
    assert "## Sources" in md
    assert "Deep Learning Guide" in md
    assert "9/10" in md
    assert "tutorial" in md
    assert "20 min" in md
    assert "2024" in md
    assert "Transformers are key" in md
    assert "neural-nets" in md
    assert "attention" in md
    assert "example.com" in md  # URL appears


def test_sources_ordered_by_score_descending():
    """Sources are sorted by learning_efficiency_score descending."""
    sources = [
        _make_source("Medium", 5),
        _make_source("Best", 9),
        _make_source("Worst", 3),
    ]
    result = _make_result(sources=sources)
    md = generate_markdown(result)
    best_pos = md.index("Best")
    medium_pos = md.index("Medium")
    worst_pos = md.index("Worst")
    assert best_pos < medium_pos < worst_pos


